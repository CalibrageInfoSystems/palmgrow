//Missing Tress Info model for Alerts
public class MissingTressInfo {

    private String plotCode;
    private String farmerCode;
    private String firstName;
    private String middleName;
    private String lastName;
    private String mandalName;
    private String villageName;
    private String saplingsplanted;
    private String currentTrees;
    private String missingTrees;
    private String percent;

    public String getPlotCode() {
        return plotCode;
    }

    public void setPlotCode(String plotCode) {
        this.plotCode = plotCode;
    }

    public String getFarmerCode() {
        return farmerCode;
    }

    public void setFarmerCode(String farmerCode) {
        this.farmerCode = farmerCode;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getMandalName() {
        return mandalName;
    }

    public void setMandalName(String mandalName) {
        this.mandalName = mandalName;
    }

    public String getVillageName() {
        return villageName;
    }

    public void setVillageName(String villageName) {
        this.villageName = villageName;
    }

    public String getSaplingsplanted() {
        return saplingsplanted;
    }

    public void setSaplingsplanted(String saplingsplanted) {
        this.saplingsplanted = saplingsplanted;
    }

    public String getCurrentTrees() {
        return currentTrees;
    }

    public void setCurrentTrees(String currentTrees) {
        this.currentTrees = currentTrees;
    }

    public String getMissingTrees() {
        return missingTrees;
    }

    public void setMissingTrees(String missingTrees) {
        this.missingTrees = missingTrees;
    }

    public String getPercent() {
        return percent;
    }

    public void setPercent(String percent) {
        this.percent = percent;
    }
}
